LL dp[26];

void  print(char *str, LL len, LL n)
{
    if(n == 1)
    {
        printf(str);
        return;
    }
    LL cnt[30] = {0};
    LL num = 1, den = 1;
    int c;
    LL j;
    int i;
    for(i = len - 1, j = 1; i >= 0; i--, j++)
    {
        c = str[i] - 'a';
        den *= (++cnt[c]);
        num *= j;
        dp[i] = num / den;
    }
    if(dp[0] < n)
    {
        printf("Impossible");
        return;
    }
    i = 0;
    while(i + 1 < len && dp[i + 1] >= n)
        putchar(str[i++]);
    j = i + 1;

    while(str[i] >= str[j])
        j++;
    swap(str[i], str[j]);
    print(str + i, len - i, n - dp[i + 1]);
}


